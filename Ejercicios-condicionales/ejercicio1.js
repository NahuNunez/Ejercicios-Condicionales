//numero positivo o negativo. Escribir un programa que determine si un numero ingresado por el usuario es positivo, negativo o cero

let numero = parseInt (prompt("Ingrese un numero")); /* parseint toma numero entero (necesario) */

if (numero>0){
    alert("El numero es positivo");
}
else if (numero == 0){
    alert("Su numero es 0");
}
else{
    alert("Su numero es negativo"); 
}



    
